    private void btnArregloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnArregloActionPerformed
        txtCola.setEnabled(true);
        txtPrio.setEnabled(true);
        txtDato.setEnabled(true);
        btnAdd.setEnabled(true);
        btnDel.setEnabled(true);
        txtNum.setEnabled(false);
        btnArreglo.setEnabled(false);
        cola.CrearArreglo(Integer.parseInt(txtNum.getText()));
    }//GEN-LAST:event_btnArregloActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        NodoP nuevo = new NodoP();
        nuevo.setDato(txtDato.getText());
        nuevo.setPrioridad(Integer.parseInt(txtPrio.getText()));
        cola.Agregar(nuevo);
        txtDato.setText("");
        txtPrio.setText("");
        txtCola.setText(cola.toString());
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
        cola.Eliminar();
        txtCola.setText(cola.toString());
    }//GEN-LAST:event_btnDelActionPerformed

